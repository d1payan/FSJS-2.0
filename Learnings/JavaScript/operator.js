// Assignment Operator => =
let x = 5;
let y = 2;

// Arithmetic Operator => +, - , * , /, %
let z = x + y;
console.log(z); // 7

console.log(x + y); // 7
console.log(x - y); // 3
console.log(x * y); // 10
console.log(x / y); // 2.5
console.log(x % y); // 1

// Comparison Operator => >, <, >=, <=, ==, ===
let a = 10;
let b = 11;

console.log(a > b); // false
console.log(a < b); // true
console.log(a >= b); // false
console.log(a <= b); // true

let abc = 20;
let xyz = '20';

console.log(abc == xyz); // true => Because == operator compares the value but not the data type
console.log(abc === xyz); // false => Because === operator compares both the value and data type
