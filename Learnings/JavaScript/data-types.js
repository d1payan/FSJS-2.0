// Data Types
// Primitive Data Types => Can have only single value

// Number Values
5;
8.5;

// String Values
('Dipayan');
('Biswas');

// Boolean Values
true;
false;

// Empty Values
null;
undefined;

// Non-Primitive Data Types => Can have multiple values

// Array
[1, 2.8, 7, 'dipayan', true, 'biswas']; // Array index starts from 0

// Object
{
	key: value;
	fname: 'Dipayan';
	lname: 'Biswas';
}
